#include <stdio.h>
int main (){
    int a = 1;
    int b = 1; 
    int c = a+b; // add a and b, store result in c
    printf("%d",c);

    int d = a-b; // subtract b from a, store result in d
    printf("%d",d);
    int e = a*b; // multiply a and b, store result in e
    printf("%d ",e);

    int f = a/b; // divide a by b, store result in f
    printf("%d",f);

    return 0;
    
}